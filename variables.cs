using System;

class VariableExamples
{
    static void Main(string[] args)
    {
        // Integer type
        int age = 25;

        // Float type
        float pi = 3.14f;

        // Double type
        double largeNumber = 12345.6789;

        // Boolean type
        bool isCSharpFun = true;

        // Char type
        char initial = 'C';

        // String type
        string message = "C# is strongly typed!";

        // Outputting variables
        Console.WriteLine("Integer: " + age);
        Console.WriteLine("Float: " + pi);
        Console.WriteLine("Double: " + largeNumber);
        Console.WriteLine("Boolean: " + isCSharpFun);
        Console.WriteLine("Character: " + initial);
        Console.WriteLine("String: " + message);
    }
}
