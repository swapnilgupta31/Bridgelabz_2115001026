using System;

class DivisibilityChecker
{
    static void Main(string[] args)
    {
        // Prompt the user for input
        Console.WriteLine("Enter a number to check divisibility by 5:");

        // Get input value for the number
        int number = int.Parse(Console.ReadLine());

        // Check divisibility and display the result
        if (number % 5 == 0)
        {
            Console.WriteLine("Is the number " + number + " divisible by 5? Yes");
        }
        else
        {
            Console.WriteLine("Is the number " + number + " divisible by 5? No");
        }
    }
}
